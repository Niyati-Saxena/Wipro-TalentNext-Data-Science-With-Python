# WAP to remove a given item from the set
set1 = {1,2,3,4,5,6}
n = int(input("Enter the element to be removed: "))
if n in set1:
    set1.remove(n)
else:
    print("Element already not present in the set")    
print(set1) 
# ---------------------------------------------------------------------------------------------------------
# WAP to create an intersection of sets
set1 = {1,2,3,4,5,6,7}
set2 = {4,5,6,7,8,9,10}
set3 = set1.intersection(set2) 
# or
set3 = set1 & set2
print(set3)        
# ----------------------------------------------------------------------------------------------------------
# WAP to create a union of the sets
set1 = {1,2,3,4,5,6,7}
set2 = {4,5,6,7,8,9,10}
set3 = set1.union(set2)
set3 = set1 | set2
print(set3)
# ----------------------------------------------------------------------------------------------------------
# WAP to find max and min value in a set
set2 = {4,5,6,7,8,9,10}
print(max(set2))
print(min(set2))
